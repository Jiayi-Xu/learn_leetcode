class Solution(object):
    def generateParenthesis(self, n):
        """
        :type n: int
        :rtype: List[str]
        """
        res = []
        def recursion(left, right, n, s):
            if left==n and right==n:
                res.append(s)
                return

            if left < n:
                recursion(left + 1, right, n, s +  "(")
            if left > right and right <n:
                recursion(left, right + 1, n, s +  ")")

        recursion(0, 0, n, "")

        return res
